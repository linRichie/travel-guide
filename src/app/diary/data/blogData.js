// 根据 id 获取博客文章
export const getBlogById = (id) => {
  return blogPosts.find(post => post.id === Number(id));
};

// 获取所有标签
export const getAllBlogTags = () => {
  const tags = new Set();
  blogPosts.forEach(post => {
    post.tags.forEach(tag => tags.add(tag));
  });
  return Array.from(tags);
};
